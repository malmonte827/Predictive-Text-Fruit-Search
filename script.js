inputField.onkeyup = search
function search() {
	let results = [];
	let input = inputField.value;
	
	 if(input.length){
		results = fruit.filter((value)=>{
		 return value.toLowerCase().includes(input.toLowerCase())
		})};
	showSuggestions(results)
}
	
function showSuggestions(results) {
 const content = results.map((value)=>{
	return '<li onclick = useSuggestion(this)>' + value + "</li>"
 })
 suggestions.innerHTML = content.join('');
}

function useSuggestion(value){
	inputField.value = value.innerHTML 
}